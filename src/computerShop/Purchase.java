    private void btnPurchaseActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPurchaseActionPerformed
        try {
            if (cmbProduct.getSelectedIndex() > 0) {
                pst = conn.prepareStatement("update product set qty=? where p_id=?");
                pst.setInt(1, Integer.parseInt(txtQuantity.getText()));
                pst.setString(2, cmbProduct.getSelectedItem().toString());
                pst.executeUpdate();

            } else {
                pst = conn.prepareStatement("insert into product "
                        + "(pname, model, price, qty, p_img) values (?,?,?,?,?)");
                pst.setString(1, cmbProduct.getSelectedItem().toString());
                pst.setString(2, txtModel.getText());
                pst.setInt(3, Integer.parseInt(txtPurchase.getText()));
                pst.setInt(4, Integer.parseInt(txtQuantity.getText()));
                pst.setBytes(5, product_image);
                pst.executeUpdate();
            }

            pst = conn.prepareStatement("select * from product where pname=?");
            pst.setString(1, cmbProduct.getSelectedItem().toString());
            rs = pst.executeQuery();
            rs.next();
            rs.getInt(1);
            String s = String.valueOf(rs.getInt(1));
            cmbProduct.setSelectedItem(s);

            pst = conn.prepareStatement("insert into purchase "
                    + "(p_id, model, price, qty, pdate) values (?,?,?,?,?)");
            pst.setInt(1, Integer.parseInt(cmbProduct.getSelectedItem().toString()));
            pst.setString(2, txtModel.getText());
            pst.setInt(3, Integer.parseInt(txtPurchase.getText()));
            pst.setInt(4, Integer.parseInt(txtQuantity.getText()));
            pst.setString(5, ((JTextComponent) txtDate.getDateEditor().getUiComponent()).getText());
            pst.executeUpdate();

            JOptionPane.showMessageDialog(rootPane, "Purchase saved!!");

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_btnPurchaseActionPerformed

